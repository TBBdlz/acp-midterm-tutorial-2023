package yingyongwatthanakit.metee;

public class Condition {

	public static void main(String[] args) {
		boolean p = true;
		boolean q = false;

		// Python3 : or, and, not
		// or <--> ||
		// and <--> &&
		// not <--> !
		System.out.println(p || q);
		System.out.println(p && q);
		System.out.println(!p);
		System.out.println(!q);
	}
	
}
